/** Build a single searchable string from a product. */
export function searchableText(p: Product): string {
  return [
    p.nameAr,
    p.nameEn,
    p.brand,
    p.brandAr ?? "",
    p.badge,
    p.category,
    p.description,
    ...p.features,
    ...(p.applications ?? []),
    ...p.keywords,
  ]
    .join(" ")
    .toLowerCase();
}
